#include "libft.h"

int			ft_atoi(const char *str)
{
	int			i;
	int			signe;
	int			nbr;

	i = 0;
	signe = 1;
	nbr = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\r'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		signe = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10;
		nbr = nbr + (int)str[i] - '0';
		i++;
	}
	return (nbr * signe);
}
